    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalmanagementsystem?useSSL=false","root","root");
            PreparedStatement pst = conn.prepareStatement("insert into equipments"+"(name,company,number,installationdate)"+" values(?,?,?,?)");
            pst.setString(1,name.getText());
            pst.setString(2,company.getText());
            pst.setString(3,number.getText());
            pst.setString(4,date.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Machine Added Sucessfully");
            showTableData();
            name.setText(null);
            company.setText(null);
            number.setText(null);
            date.setText(null);
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
                                        
    }//GEN-LAST:event_addActionPerformed

    private void equiptableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_equiptableMouseClicked
        // TODO add your handling code here:
         int SelectedRow =equiptable.getSelectedRow();
        DefaultTableModel table=(DefaultTableModel)equiptable.getModel();
        name.setText(table.getValueAt(SelectedRow, 0).toString());
        company.setText(table.getValueAt(SelectedRow, 1).toString());
        number.setText(table.getValueAt(SelectedRow, 2).toString());
        date.setText(table.getValueAt(SelectedRow, 3).toString());
    }//GEN-LAST:event_equiptableMouseClicked

